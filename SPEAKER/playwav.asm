PLAYLOOP: LODSB                 ; PLAY TONE              
          OUT   42H,AL                             
          mov   DX,BX                              
DELAYLOOP:DEC   DX                                 
          JNZ   DELAYLOOP                          
          LOOP  PLAYLOOP        ; TURN ON SPEAKER        
          IN    AL,61H                             
          OR    AL,03H                             
          OUT   61H,AL          ; ENABLE INTERRUPTS      
          STI   
          RET   
SPEAKPLAY ENDP  


