; DS:SI must point to the sound file buffer on entry.  
; CX must hold the length of the sound buffer on entry.
; DX must hold the calculated delay time on entry.     

SPEAKPLAY PROC                  ; SET SEGMENT POINTER    
          mov   DS,AX                              
          mov   BX,DX           ; SET UP TIMER FREQUENCY 
          mov   AL,90H                             
          OUT   43H,AL          ; TURN ON SPEAKER        
